public class Coordenadas {
    public double ejeX;
    public double ejeY;

    public Coordenadas(double ejeX, double ejeY){
        this.ejeX = ejeX;
        this.ejeY = ejeY;
    }

    public void setEjeX(double newEjeX){
        ejeX = newEjeX;
    }

    public void setEjeY(double newEjeY){
        ejeY = newEjeY;
    }
}
